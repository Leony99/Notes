//Comparison operators
//Return a boolean value

let x;

console.log(x == 10); //equal value
console.log(x === 10); //equal value and equal type
console.log(x != 10); //not equal value
console.log(x !== 10); //not equal value or not equal type
console.log(x > 10); //higher
console.log(x >= 10); //higher or equal
console.log(x < 10); //lower
console.log(x <= 10); //lower or equal