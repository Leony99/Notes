//Arithmetic operators

let x;

x = 2 + 2;
x = 2 - 2;
x = 2 * 2;
x = 2 / 2;
x = 2 ** 2; //Exponentiation
x = 2 % 2; //Division remainder

//Increment operators
x++; //i = i+1 / post-increment
++x; //i = i+1 / pre-increment

//Decrement operators
x--; //i = i-1 / post-decrement
--x; //i = i-1 / pre-decrement